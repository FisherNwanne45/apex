                                <h1>How Can I Protect Against Identity Theft?</h1>
                            </div>
                            <p><span>We believe that one of the best ways to fight identity theft is to prevent it in
                                    the first place. Here are some things that you can do to prevent and protect your
                                    identity being stolen:</span></p><br />
                            <h3>Regarding the Internet: </h3>
                            <ul>
                                <li><span>Be wary of providing personal information over the Internet. Do not share
                                        personal information over the Internet with companies or people that you don't
                                        know.</span></li>
                                <li><span>Be alert with emails. Never send important personal information via email.
                                        Never login to online banking via email. If there is a link, make sure that the
                                        URL is the correct URL</span></li>
                                <li><span>Install virus protection and spyware protection on your computer. Protect your
                                        computer from viruses and spywares.</span></li>
                                <li><span>Choose passwords carefully. Do not choose dictionary word or numbers
                                        associated with your address or birthday. Do not use the same password for
                                        important accounts.</span></li>
                            </ul><br />
                            <h3>Regarding your mail:</h3>
                            <ul>
                                <li><span>Don't put outgoing mail in your mailbox. Drop your mail into a secure,
                                        official Postal Service collection box. Discard mail appropriately. Tear up
                                        unwanted financial solicitations before throwing them away. Destroy all
                                        sensitive financial documents before throwing them away. Consider a home paper
                                        shredder.</span></li>
                            </ul><br />
                            <h3>Regarding your credit card and ATM card:</h3>
                            <ul>
                                <li><span>Cancel credit card accounts that you no longer use. Cut up these cards and
                                        make sure the numbers are not recognizable. Don't keep old credit cards or ATM
                                        cards around.</span></li>
                                <li><span>Report lost or stolen credit cards immediately. Call each credit card issuer
                                        and ask to have the stolen card accounts closed and new ones opened to replace
                                        them.</span></li>
                                <li><span>Be careful with your ATM and credit card receipts. Never throw away these
                                        receipts in a public trashcan. Guard your Personal Identification Number (PINs).
                                        Memorize them. Don't write your PIN on your ATM card and don't keep your PIN
                                        with your cards.</span></li>
                                <li><span>Sign the back of your credit card and ATM card. Do this as soon as you receive
                                        the new cards.</span></li>
                                <li><span>Keep a list of credit cards and their company phone numbers in a safe place in
                                        case they are lost or stolen.</span></li>
                                <li><span>Never lend your card to other people.</span></li>
                            </ul>
                            <h3>Regarding your checks:</h3>
                            <ul>
                                <li><span>Don't preprint your important personal information on your checks. Your checks
                                        should not have your driver's license number, Social Security number, or
                                        telephone number on them.</span></li>
                                <li><span>Report lost or stolen checks immediately.</span></li>
                                <li><span>Store cancelled checks and new checks safely.</span></li>
                            </ul>
                            <h3>Regarding telephone scams:</h3>
                            <ul>
                                <li><span>Be alert on telephone scams. Be wary of providing personal information over
                                        the phone. Notify the appropriate financial institutions of any suspicious phone
                                        inquiries.</span></li>
                            </ul>
                            <h3>Other things you should do to protect yourself:</h3>
                            <ul>
                                <li><span>Carry only what you need. Do not carry your Social Security card, passport or
                                        birth certificate unless needed that day. The less personal information you have
                                        with you, the better off you will be if your wallet or purse gets stolen.</span>
                                </li>
                                <li><span>Keep track of bills. If a regular bill did not arrive on schedule, call the
                                        company to find out why. If your bills include any suspicious items, don't
                                        ignore them. Investigate them immediately.</span></li>
                                <li><span>Review your credit report periodically to make sure the information is
                                        correct.</span></li>
                            </ul>
                            <p><span>You can obtain a free credit report from <span><a
                                            href="https://www.annualcreditreport.com/cra/index.jsp" target="_blank"
                                            class="confirm">Annual Credit Report</a></span>. This site is run by the
                                    major credit bureaus and also offers useful tools to monitor your credit
                                    files.</span></p>
