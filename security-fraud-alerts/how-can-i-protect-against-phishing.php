                                <h1>How Can I Protect Against Phishing?</h1>
                            </div>
                            <p><span>There are many different types of email fraud, which makes enforcement and
                                    prosecution difficult. The best way to protect your identity and your accounts is to
                                    be informed and cautious. And, remember - we will NEVER ask you to enter personal or
                                    account information in email or download an attachment from email. Here are some
                                    guidelines to help you protect yourself:</span></p><br />
                            <p><span>1. Be suspicious of emails with urgent requests for personal financial information.
                                    Many fake emails use strong and often threatening language to convince you that
                                    something bad will happen (i.e., your account will be shut down) if you do not click
                                    the provided link immediately and update or validate your account information.
                                    Misspelled or misused words should also be warning signs. If we need to convey
                                    urgent requests, we will send you a physical mail.</span></p>
                            <p><span>2. Do not reply, click or enter information if you receive a suspicious email.
                                    "Phishing" emails typically ask for login information, Social Security Numbers or
                                    account numbers. You should only communicate this information from your secure inbox
                                    or by phone. We will never ask you to disclose it in email.</span></p>
                            <p><span>3. Don't click unfamiliar links or fill out forms within email messages. If you
                                    don't recognize a web address included in an email, you should open a new browser
                                    and type in an address you know. The majority of fraudulent emails will either have
                                    a copy of a web page included as part of the email or link to fake copies of the
                                    <?php echo $name; ?> home page or Login page. If you're suspicious, type the familiar
                                    address, like "www.<?php echo $url; ?>," into your browser to be certain you go to our
                                    site.</span></p>
                            <p><span>4. Access your account through <?php echo $name; ?> home page. Before you click "login",
                                    always make sure that you are on the correct webpage by checking the address bar on
                                    your browser even if you reached the page from your browser bookmark (some
                                    fraudulent emails are so potent that they will alter your bookmark.) Make sure that
                                    the address is secure (https://) the address is either
                                    https://apextrustbk.securexfr.com or https://secure.<?php echo $url; ?></span></p>
                            <p><span>5. Log in regularly to check your online accounts. It's wise to check your accounts
                                    at least once a week. If you don't check them very often, you may allow criminals a
                                    lot of time to do damage before you realize it. We also recommend that you review
                                    your monthly statements for accuracy and order copies of your credit report from
                                    each of the three major credit bureaus at least once a year.</span></p>
                            <p><span>6.Ensure your browser and security software information is updated. Some suspicious
                                    email can contain viruses or hidden programs that secretly track and report your
                                    Internet activity and use. Anti-virus software, firewall protection and software
                                    patches from your operating system provider (i.e., Microsoft or Apple) can help
                                    prevent criminals from monitoring your online activities. Also, be sure to keep your
                                    software up to date by installing any manufacturer-issued security patches.</span>
                            </p><br />
                            <p><span><strong>If you use wireless devices, such as smart phones or PDAs, be sure to
                                        enable Wireless Encryption Protocol (WEP).</strong></span></p><br />
                            <p><span>7. Safeguard your online activity. You should always log out of an online banking
                                    session anytime you step away from your computer. And, be sure to shut off or
                                    disconnect your computer from the Internet when not in use.</span></p><br />
                            <p><span>If you use a computer with public access, such as in a library or Internet cafe,
                                    please ensure that any User IDs and passwords you enter are not saved on that
                                    computer. You should also delete all temporary internet files after use. (For
                                    Internet Explorer, go to Tools -&gt; Internet Options and click on "Delete Cookies"
                                    and "Delete Files" under the "Temporary Internet files" section.)</span></p><br />
                            <p><span>8. Protect your online identity. Criminals can get your email address in many ways
                                    - searching websites and chat rooms, buying online address lists, etc. You can be
                                    prepared by creating more than one email address. Use one for general web use (i.e.,
                                    chat rooms, newsletters, etc.) with the expectation that it will receive spam. Keep
                                    at least one more for private uses like secure communications with your online
                                    banking service.</span></p><br />
                            <p><span>Also, avoid entering your email address at unsecured sites if possible. Many
                                    websites don't require your email address for registration or ordering purposes, but
                                    they ask for it so they can add you to mailing lists for newsletters, sales, etc.
                                    Criminals and spammers buy these mailing lists to use for "phishing"
                                    purposes.</span></p><br />
                            <p><span>9. Report any attempts to steal your private information. Anyone who has access to
                                    your personal information can be a threat. Keeping us informed will help all of our
                                    customers in the future.</span></p><br />
                            <h3>Report Fraud.</h3>
                            <p><span>You should also contact and file a complaint at the Internet Crime Complaint
                                    Center, a partnership between the FBI and the National White Collar Crime
                                    Center.</span></p><br />
                            <p><span>If you suspect that you have been a victim of identity theft, Read about how to
                                    recover from identity theft.</span></p><br />
